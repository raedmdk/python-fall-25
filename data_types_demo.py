# --- Literal Example ---
# A literal is a raw value directly used in the code
print("Literal:", 10)   # This prints the number 10 as a literal

# --- Integer Example ---
# An integer is a whole number, positive or negative, without decimals
integer_value = -20
print("Integer:", integer_value)

# --- Float Example ---
# A float is a number with a decimal point
temperature = 25.5
print("Float:", temperature)

# --- String Example ---
# A string is a sequence of characters enclosed in quotes
student_name = "Raed Kiswani"
print("String:", student_name)

# --- Boolean Example ---
# A boolean represents True or False values
is_enrolled = True
print("Boolean:", is_enrolled)

